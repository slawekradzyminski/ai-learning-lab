import { createReadStream, promises as fs } from 'node:fs';
import { createGunzip } from 'node:zlib';
import { createInterface } from 'node:readline';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const groups = {
  people: ['man', 'woman', 'boy', 'girl', 'child', 'adult', 'father', 'mother', 'son', 'daughter', 'brother', 'sister', 'uncle', 'aunt', 'family'],
  royalty: ['king', 'queen', 'prince', 'princess', 'monarch', 'royal', 'throne', 'palace', 'crown', 'emperor', 'empress', 'duke', 'duchess', 'lord', 'lady'],
  animals: ['animal', 'dog', 'puppy', 'cat', 'kitten', 'horse', 'cow', 'lion', 'tiger', 'bear', 'wolf', 'fox', 'bird', 'eagle', 'fish'],
  places: ['city', 'village', 'street', 'road', 'avenue', 'highway', 'car', 'truck', 'bus', 'train', 'plane', 'airport', 'station', 'bridge', 'river'],
  geography: ['france', 'paris', 'germany', 'berlin', 'italy', 'rome', 'england', 'london', 'japan', 'tokyo', 'china', 'beijing', 'russia', 'moscow', 'poland', 'warsaw', 'spain', 'madrid'],
  food: ['breakfast', 'lunch', 'dinner', 'cereal', 'coffee', 'tea', 'bread', 'apple', 'orange', 'fruit', 'vegetable', 'rice', 'pasta', 'pizza', 'restaurant'],
  technology: ['computer', 'software', 'hardware', 'code', 'database', 'server', 'cloud', 'network', 'internet', 'programmer', 'engineer', 'robot', 'algorithm', 'data', 'model'],
  knowledge: ['science', 'physics', 'chemistry', 'biology', 'mathematics', 'teacher', 'student', 'school', 'university', 'book', 'novel', 'doctor', 'nurse', 'hospital', 'medicine'],
  actions: ['walk', 'walked', 'run', 'ran', 'swim', 'swam', 'eat', 'ate', 'drink', 'drank', 'sing', 'sang', 'write', 'wrote', 'read'],
  qualities: ['big', 'small', 'fast', 'slow', 'hot', 'cold', 'happy', 'sad', 'good', 'bad', 'young', 'old', 'strong', 'weak', 'rich', 'poor'],
  grammar: ['hand', 'hands', 'foot', 'feet', 'cars', 'dogs', 'cats', 'children', 'mouse', 'mice', 'person', 'people'],
  time: ['morning', 'evening', 'night', 'day', 'spring', 'summer', 'autumn', 'winter', 'yesterday', 'today', 'tomorrow', 'week', 'month', 'year', 'time'],
};

const sourcePath = process.argv[2];
if (!sourcePath) throw new Error('Usage: node scripts/build-glove-word-space.mjs <glove-gensim.gz>');

const requested = [];
const metadata = new Map();
for (const [category, words] of Object.entries(groups)) {
  for (const word of words) {
    if (metadata.has(word)) continue;
    metadata.set(word, category);
    requested.push(word);
  }
}

const wanted = new Set(requested);
const found = new Map();
const lines = createInterface({ input: createReadStream(sourcePath).pipe(createGunzip()), crlfDelay: Infinity });
for await (const line of lines) {
  const firstSpace = line.indexOf(' ');
  if (firstSpace < 0) continue;
  const word = line.slice(0, firstSpace);
  if (!wanted.has(word)) continue;
  const vector = line.slice(firstSpace + 1).trim().split(/\s+/).map(Number);
  if (vector.length !== 50 || vector.some((value) => !Number.isFinite(value))) continue;
  found.set(word, vector);
  if (found.size === wanted.size) break;
}

const missing = requested.filter((word) => !found.has(word));
if (missing.length) throw new Error(`Missing GloVe words: ${missing.join(', ')}`);

const rows = requested.map((word) => ({
  word,
  category: metadata.get(word),
  vector: found.get(word).map((value) => Number(value.toFixed(5))),
}));

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = path.join(projectRoot, 'src/features/learning/course/gloveWordEmbeddings.ts');
const output = `// Generated by scripts/build-glove-word-space.mjs from Stanford GloVe 6B 50d.\n// Source data: https://nlp.stanford.edu/projects/glove/ (PDDL 1.0).\n\nexport type GloveWord = { word: string; category: string; vector: number[] };\n\nexport const GLOVE_MODEL = {\n  label: 'GloVe 6B · Wikipedia 2014 + Gigaword 5',\n  dimensions: 50,\n  license: 'Public Domain Dedication and License 1.0',\n  source: 'https://nlp.stanford.edu/projects/glove/',\n} as const;\n\nexport const GLOVE_WORDS: GloveWord[] = ${JSON.stringify(rows)};\n`;
await fs.writeFile(outputPath, output);
console.log(`Wrote ${rows.length} real ${rows[0].vector.length}D word vectors to ${outputPath}`);
